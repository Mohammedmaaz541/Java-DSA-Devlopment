package module_2_fundamentals.basics;

public class Unaryoperator {
    public static void main(String [] args){
        //pre increament
        int a=45;
        int b=++a;
        System.out.println(a);
        System.out.println(b);

        //post increament
        int c=5;
        int d=c++;
        System.out.println(c);
        System.out.println(d);
    }

}
